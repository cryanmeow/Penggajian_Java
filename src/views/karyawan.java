    private void datatable(){
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("ID");
        tbl.addColumn("Nama");
        tbl.addColumn("Alamat");
        tbl.addColumn("NOHP");
        tbl.addColumn("Tempat Lahir");
        tbl.addColumn("Tanggal Lahir");
        tbl.addColumn("Jenis Kelamin");
        tbl.addColumn("Kode Jabatan");
        tbl.addColumn("Jabatan");
        tbl.addColumn("Status");
        tbl.addColumn("Jumlah Anak");
        dataTable.setModel(tbl);
        try{
            Statement statement = (Statement) connect.getConnection().createStatement();
            ResultSet res = statement.executeQuery("Select * from tbl_karyawan as k INNER JOIN tbl_jabatan as j ON k.kode_jabatan = j.kode_jabatan");
            while(res.next())
            {
                tbl.addRow(new Object[]{
                res.getString("id_karyawan"),
                res.getString("nama_karyawan"),
                res.getString("alamat"),
                res.getString("no_hp"),
                res.getString("tempat_lahir"),
                res.getString("tanggal_lahir"),
                res.getString("jenis_kelamin"),
                res.getString("kode_jabatan"),
                res.getString("nama_jabatan"),
                res.getString("status"),
                res.getString("jml_anak"),
                });
                dataTable.setModel(tbl);
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
    
    
    private void tampilJabatan(){
        try{
            Statement panggil = (Statement) connect.getConnection().createStatement();
            ResultSet res = panggil.executeQuery("SELECT * FROM tbl_jabatan");
            while(res.next()){
                cmb_kodeJabatan.addItem(res.getString("kode_jabatan"));
            }
            panggil.close();
        } catch(Exception e){
            JOptionPane.showMessageDialog(null,"GAGAL", "error", JOptionPane.WARNING_MESSAGE);
        }
    }
    
