    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        String nama_karyawan = txt_namaKaryawan.getText();
        String alamat = txt_alamat.getText();
        String no_hp = txt_noHp.getText();
        String tempat_lahir = txt_tempatLahir.getText(); 
        SimpleDateFormat tanggal;
        tanggal = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal_lahir = tanggal.format(date.getDate());
        String jenis_kelamin = (String) cmb_jenisKelamin.getSelectedItem();
        String kode_jabatan = (String) cmb_kodeJabatan.getSelectedItem(); 
        String status = (String) cmb_status.getSelectedItem(); 
        String jml_anak = txt_jumlahAnak.getText();
        try {
            Statement statement = (Statement) connect.getConnection().createStatement();
            statement.executeUpdate("insert into tbl_karyawan (nama_karyawan, alamat, no_hp, tempat_lahir, tanggal_lahir, jenis_kelamin, kode_jabatan, status, jml_anak)VALUES('" + nama_karyawan + "','" + alamat + "','" + no_hp + "','" + tempat_lahir + "','" + tanggal_lahir + "','" + jenis_kelamin + "','" + kode_jabatan + "','" + status + "','" + jml_anak + "');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (Exception t) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan" + t.getMessage());
        }
        datatable();
    }//GEN-LAST:event_btn_simpanActionPerformed

