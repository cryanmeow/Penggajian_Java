    private void datatable(){
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("idGaji");
        tbl.addColumn("IdKaryawan");
        tbl.addColumn("Nama Karyawan");
        tbl.addColumn("Gaji Kotor");
        tbl.addColumn("Jumlah Lembur");
        tbl.addColumn("Pajak");
        tbl.addColumn("Total Tunjangan");
        tbl.addColumn("Gaji Bersih");
        tbl.addColumn("Bulan");
        tbl.addColumn("Tahun");
        dataTable.setModel(tbl);
        try{
            Statement statement = (Statement) connect.getConnection().createStatement();
            ResultSet res = statement.executeQuery("Select * from tbl_penggajian as p INNER JOIN tbl_karyawan as k ON p.id_karyawan = k.id_karyawan");
            while(res.next())
            {
                tbl.addRow(new Object[]{
                res.getString("id_penggajian"),
                res.getString("id_karyawan"),
                res.getString("nama_karyawan"),
                res.getString("gaji_kotor"),
                res.getString("jumlah_lembur"),
                res.getString("pajak"),
                res.getString("total_tunjangan"),
                res.getString("gaji_bersih"),
                res.getString("bulan"),
                res.getString("tahun"),
                });
                dataTable.setModel(tbl);
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
