    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        String value = jComboBox1.getSelectedItem().toString();
        String[] arrValue = value.split(" - ");
        String id_karyawan = arrValue[0];
        String gaji_kotor = txt_gajiKotor.getText();
        String jumlah_lembur = txt_gajiLembur.getText();
        String pajak = txt_pajak.getText();
        String tunjangan_istri = txt_tunjanganIstri.getText();
        String tunjangan_anak = txt_tunjanganAnak.getText();
        Integer total_tunjangan = Integer.parseInt(tunjangan_istri)+Integer.parseInt(tunjangan_anak);
        String gaji_bersih = txt_gajiBersih.getText();
        String bulan = txt_bulan.getText();
        String tahun = txt_tahun.getText();
        try {
            Statement statement = (Statement) connect.getConnection().createStatement();
            statement.executeUpdate("insert into tbl_penggajian (id_karyawan, gaji_kotor, jumlah_lembur, pajak, total_tunjangan, gaji_bersih, bulan, tahun)VALUES('" + id_karyawan + "','" + gaji_kotor + "','" + jumlah_lembur + "','" + pajak + "','" + total_tunjangan + "','" + gaji_bersih + "','" + bulan + "','" + tahun + "');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (Exception t) {
            JOptionPane.showMessageDialog(null, "Data gagal disimpan" + t.getMessage());
        }
        datatable();
    }//GEN-LAST:event_btn_simpanActionPerformed

    
