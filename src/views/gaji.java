    private void hitungGaji(){
        Integer gaji_pokok = Integer.parseInt(txt_gajiPokok.getText());
        Integer total_gaji_lembur = Integer.parseInt(txt_gajiLembur.getText());
        Integer tunjangan_istri = Integer.parseInt(txt_tunjanganIstri.getText());
        Integer tunjangan_anak = Integer.parseInt(txt_tunjanganAnak.getText());
        Integer gaji_kotor = gaji_pokok+tunjangan_istri+tunjangan_anak;
        Integer pajak_karyawan = gaji_kotor*10/100;
        Integer gaji_bersih = (gaji_kotor+total_gaji_lembur)-pajak_karyawan;
        
        txt_gajiKotor.setText(gaji_kotor.toString());
        txt_pajak.setText(pajak_karyawan.toString());
        txt_gajiBersih.setText(gaji_bersih.toString());
    }
    
    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        String value = jComboBox1.getSelectedItem().toString();
        String[] arrValue = value.split(" - ");
        String id_karyawan = arrValue[0];
        
        try{
            Statement statement = (Statement) connect.getConnection().createStatement();
            ResultSet result = statement.executeQuery("SELECT * FROM tbl_karyawan WHERE id_karyawan='"+id_karyawan+"' ");
            if(result.next()){
                String kode_jabatan = result.getString("kode_jabatan");
                String status_karyawan = result.getString("status");
                String jml_anak = result.getString("jml_anak");
                try{
                    ResultSet result2 = statement.executeQuery("SELECT * FROM tbl_jabatan WHERE kode_jabatan='"+kode_jabatan+"' ");
                    if(result2.next()){
                        String nama_jabatan = result2.getString("nama_jabatan");
                        String gaji_pokok = result2.getString("jml_gaji_pokok");
                        String upah_lembur = result2.getString("upah_lembur");
                        Integer tunjangan_istri;
                        Integer tunjangan_anak;
                        
                        if("BelumMenikah".equals(status_karyawan)){
                            tunjangan_istri = 0;
                            tunjangan_anak = 0;
                        }else{
                            tunjangan_istri = Integer.parseInt(gaji_pokok)*20/100;
                            if(Integer.parseInt(jml_anak) <= 2){
                                tunjangan_anak = (Integer.parseInt(gaji_pokok)*Integer.parseInt(jml_anak))*15/100;
                            }else if(Integer.parseInt(jml_anak) >= 2){
                                tunjangan_anak = (Integer.parseInt(gaji_pokok)*2)*15/100;
                            } else{
                                tunjangan_anak = 0;
                            }
                        }
                        
                        txt_gajiPokok.setText(gaji_pokok);
                        txt_upahLembur.setText(upah_lembur);
                        txt_tunjanganIstri.setText(tunjangan_istri.toString());
                        txt_tunjanganAnak.setText(tunjangan_anak.toString());
                        txt_jabatan.setText(nama_jabatan.toString());
                        
                    }
                }catch(Exception err){
                    JOptionPane.showMessageDialog(null, err.getMessage());
                }
            }
        }catch(Exception err){
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

