    private void datatable(){
        DefaultTableModel tbl= new DefaultTableModel();
        tbl.addColumn("Kode Jabatan");
        tbl.addColumn("Nama Jabatan");
        tbl.addColumn("Jumlah Gaji Pokok");
        tbl.addColumn("Upah Lembur");
        tb_jabatan.setModel(tbl);
        try{
            Statement statement = (Statement) connect.getConnection().createStatement();
            ResultSet res = statement.executeQuery("Select * from tbl_jabatan");
            while(res.next())
            {
                tbl.addRow(new Object[]{
                res.getString("kode_jabatan"),
                res.getString("nama_jabatan"),
                res.getString("jml_gaji_pokok"),
                res.getString("upah_lembur"),
                });
                tb_jabatan.setModel(tbl);
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
