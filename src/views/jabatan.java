    private void tb_jabatanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_jabatanMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tb_jabatan.getModel();
        tb_jabatan.getSelectedRow();
        Object kode_jabatan=model.getValueAt(tb_jabatan.getSelectedRow(), 0);
        kode_jabatann=(String) kode_jabatan;
        txt_kodeJabatan.setText(String.valueOf(kode_jabatan));
        Object nama_jabatan=model.getValueAt(tb_jabatan.getSelectedRow(), 1);
        txt_namaJabatan.setText(String.valueOf(nama_jabatan));
        Object jml_gaji_pokok=model.getValueAt(tb_jabatan.getSelectedRow(), 2);
        txt_jumlahGajiPokok.setText(String.valueOf(jml_gaji_pokok));
        Object upah_lembur=model.getValueAt(tb_jabatan.getSelectedRow(), 3);
        txt_upahLembur.setText(String.valueOf(upah_lembur));
    }//GEN-LAST:event_tb_jabatanMouseClicked

